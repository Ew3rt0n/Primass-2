package com.example.primass.model;

public class Convercoes {
    String valNome, valVogal, valConsoante, valData, valNomeComData, nomeNum;

    public Convercoes() {
    }

    public String getValNome() {
        return valNome;
    }

    public void setValNome(String valNome) {
        this.valNome = valNome;
    }

    public String getValVogal() {
        return valVogal;
    }

    public void setValVogal(String valVogal) {
        this.valVogal = valVogal;
    }

    public String getValConsoante() {
        return valConsoante;
    }

    public void setValConsoante(String valConsoante) {
        this.valConsoante = valConsoante;
    }

    public String getValData() {
        return valData;
    }

    public void setValData(String valData) {
        this.valData = valData;
    }

    public String getValNomeComData() {
        return valNomeComData;
    }

    public void setValNomeComData(String valNomeComData) {
        this.valNomeComData = valNomeComData;
    }

    public String getNomeNum() {
        return nomeNum;
    }

    public void setNomeNum(String nomeNum) {
        this.nomeNum = nomeNum;
    }
}
